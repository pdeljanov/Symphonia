fn make_dca_codebook<E: CodebookEntry>(
    builder: &mut CodebookBuilder,
    source: &[(u8, u8)],
) -> Codebook<E> 
where E::ValueType: From<u8> {
    let mut entries: Vec<(u8, u8)> = source.iter().cloned().filter(|&(_, l)| l > 0).collect();
    if entries.is_empty() {
        return Codebook::default();
    }
    // FFmpeg's ff_vlc_init_from_lengths with VLC_INIT_STATIC_OVERLONG logic:
    // 1. Sort by length
    entries.sort_by_key(|&(_, l)| l);
    
    let mut codes = Vec::with_capacity(entries.len());
    let mut lens = Vec::with_capacity(entries.len());
    let mut values = Vec::with_capacity(entries.len());
    
    let mut code = 0u32;
    let mut last_len = entries[0].1;
    
    for (val, len) in entries {
        code <<= len - last_len;
        codes.push(code);
        lens.push(len);
        values.push(E::ValueType::from(val));
        code += 1;
        last_len = len;
    }
    
    builder.make(&codes, &lens, &values).expect("failed to make DCA codebook")
}

