/// `Crc8` implements the CRC8 algorithm using the ITU-T I.432.1 (02/99)
/// polynominal: x^8 + x^2 + x^1 + x^0 initialized with 0.
pub struct Crc8 {
    state: u8,
}

impl Crc8 {
    pub fn new() -> Crc8 {
        Crc8 {
            state: 0
        }
    }

    /// Returns the computed CRC.
    pub fn crc(&self) -> u8 {
        self.state
    }
}

impl Monitor for Crc8 {
    #[inline(always)]
    fn process_byte(&mut self, byte: &u8) {
        self.state = CRC8_ITU_T_I_432_1[(self.state ^ byte) as usize];
    }

    #[inline(always)]
    fn process_buf_bytes(&mut self, buf: &[u8]) {
        for byte in buf {
            self.state = CRC8_ITU_T_I_432_1[(self.state ^ byte) as usize];
        }
    }
}

