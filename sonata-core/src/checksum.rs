/// `Crc16` implements the CRC16 algorithm using the ANSI polynominal: x^16 + x^15 + x^2 + 1
/// initialized with 0.
pub struct Crc16 {
    state: u16,
}

impl Crc16 {
    pub fn new() -> Crc16 {
        Crc16 {
            state: 0
        }
    }

    /// Returns the computed CRC.
    pub fn crc(&self) -> u16 {
        self.state
    }
}

impl Monitor for Crc16 {
    #[inline(always)]
    fn process_byte(&mut self, byte: &u8) {
        self.state = (self.state << 8) ^ CRC16_ANSI[((self.state >> 8) as u8 ^ byte) as usize];
    }

    #[inline(always)]
    fn process_buf_bytes(&mut self, buf: &[u8]) {
        for byte in buf {
            self.state = (self.state << 8) ^ CRC16_ANSI[((self.state >> 8) as u8 ^ byte) as usize];
        }
    }
}
